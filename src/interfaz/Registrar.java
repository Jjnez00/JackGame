    private void page(JPanel i, JPanel c) {
        i.setSize(c.getWidth(), c.getHeight());
        i.setLocation(0, 0);

        c.removeAll();
        c.add(i);
        c.revalidate();
        c.repaint();
    }

    private void comprobandoNombre() {
        nombre.getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void changedUpdate(DocumentEvent e) {
                updated();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                updated();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                updated();
            }

            public void updated() {
                BtnAceptar.setEnabled(nombre.getText().length() > 0);
            }
        });
    }

